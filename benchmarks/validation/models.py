"""Typed records for the validation framework's JSON snapshot.

The framework wraps `benchmarks.parity_timing.registry` and exports a
deterministic, host-insensitive structural view. The dataclasses below
describe the shape of each JSON file under
`benchmarks/validation/registry/`. They are intentionally stdlib-only:
the model layer itself does not import numpy, rpy2, or R / MATLAB /
Octave packages.

All dataclasses are frozen and convert to plain dicts via
`to_dict()` so the export step can serialise them with
`json.dumps(..., indent=2, sort_keys=True, ensure_ascii=True)` and
produce byte-stable output.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any

SCHEMA_VERSION = 1
SCHEMA_DESCRIPTION = (
    "Declarative validation contracts derived from "
    "benchmarks.parity_timing.registry. Phase PLS-1 of "
    "VALIDATION_FRAMEWORK_ROADMAP.md - structural data only, "
    "host-insensitive, regenerated by "
    "`python -m benchmarks.validation.export_registry --write`."
)


def _scalarise(value: Any) -> Any:
    """Coerce a `cell_params` value to a JSON-friendly scalar / list.

    The parity registry stores a handful of values as numpy arrays
    (e.g. `so_pls.n_components_per_block`); the rest are Python int /
    float / bool / str. We avoid importing numpy here (the validation
    model layer has no numpy dependency); the `tolist()` / `item()`
    protocol is enough because numpy supplies both.
    """
    if value is None or isinstance(value, (bool, int, float, str)):
        return value
    tolist = getattr(value, "tolist", None)
    if callable(tolist):
        return tolist()
    item = getattr(value, "item", None)
    if callable(item):
        return item()
    if isinstance(value, (list, tuple)):
        return [_scalarise(v) for v in value]
    if isinstance(value, dict):
        return {str(k): _scalarise(v) for k, v in sorted(value.items())}
    return str(value)


def normalise_cell_params(params: dict[str, Any]) -> dict[str, Any]:
    """Sorted, JSON-friendly copy of a MethodSpec.cell_params dict."""
    return {str(k): _scalarise(v) for k, v in sorted(params.items())}


@dataclass(frozen=True)
class MethodRecord:
    """One row in `methods.json`.

    Mirrors `benchmarks.parity_timing.registry.MethodSpec` minus the
    Python callables (`pls4all_fn`, factory lambdas) so the snapshot is
    portable and diff-stable.
    """
    name: str
    description: str
    cell_params: dict[str, Any]
    rmse_rel_tol: float
    prediction_key: str
    paper_only: str
    notes: str
    needs_x_target: bool
    needs_sample_weights: bool
    needs_labels: bool
    needs_group_assignment: bool
    has_python_reference: bool
    has_r_reference: bool
    extra_reference_roles: list[str]
    reference_kind: str
    canonical_reference_role: str | None
    comparator: str
    tolerance_quality: str

    def to_dict(self) -> dict[str, Any]:
        return {
            "name": self.name,
            "description": self.description,
            "cell_params": normalise_cell_params(self.cell_params),
            "rmse_rel_tol": float(self.rmse_rel_tol),
            "prediction_key": self.prediction_key,
            "paper_only": self.paper_only,
            "notes": self.notes,
            "needs_x_target": bool(self.needs_x_target),
            "needs_sample_weights": bool(self.needs_sample_weights),
            "needs_labels": bool(self.needs_labels),
            "needs_group_assignment": bool(self.needs_group_assignment),
            "has_python_reference": bool(self.has_python_reference),
            "has_r_reference": bool(self.has_r_reference),
            "extra_reference_roles": sorted(self.extra_reference_roles),
            "reference_kind": self.reference_kind,
            "canonical_reference_role": self.canonical_reference_role,
            "comparator": self.comparator,
            "tolerance_quality": self.tolerance_quality,
        }


@dataclass(frozen=True)
class ReferenceRecord:
    """One reference slot declared somewhere in the registry.

    Role identifies the reference factory slot - `python`, `r`, or a
    user-defined extra (`ikpls`, `mixOmics`). The library name / version
    are deliberately omitted: those are host-sensitive (read via
    `importlib.metadata.version` at adapter construction time), and the
    snapshot must be reproducible on any host.
    """
    role: str
    methods: list[str]
    is_extra: bool

    def to_dict(self) -> dict[str, Any]:
        return {
            "role": self.role,
            "is_extra": bool(self.is_extra),
            "methods": sorted(self.methods),
        }


@dataclass(frozen=True)
class DatasetRecord:
    """One synthetic dataset cell from the cross-binding orchestrator.

    The cross-binding orchestrator generates `(n, p)` cells via
    `gen_dataset_csv(n, p, seed)` - that is the executable surface we
    capture here. The seed_base mirrors
    `orchestrator.DEFAULT_SEED_BASE`.
    """
    id: str
    n: int
    p: int
    seed_base: int

    def to_dict(self) -> dict[str, Any]:
        return {
            "id": self.id,
            "n": int(self.n),
            "p": int(self.p),
            "seed_base": int(self.seed_base),
        }


