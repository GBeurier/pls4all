#!/usr/bin/env python3
"""CUDA benefit diagnostic — which methods would a CUDA backend actually help?

CUDA (cuBLAS) accelerates the GEMM/GEMV path in `cpp/src/core/linalg`. A method
benefits from GPU offload only if (a) it is GEMM/large-matrix bound and (b) the
work grows fast enough with n×p to amortise kernel-launch + host↔device copy
overhead. Elementwise / small-matrix ops (most preprocessing + augmentation)
will be *slower* on GPU at the dashboard sizes.

There is no live CUDA timing data yet (the `cuda-on` build exists but isn't
benchmarked), so this uses a PROXY available today: the **BLAS+OpenMP vs native
scalar speedup** from the committed timings. BLAS and CUDA accelerate the SAME
GEMM path, so a method that BLAS already speeds up at the largest size is the
prime CUDA candidate; a method that runs identically with/without BLAS (the
"≡ native" elementwise ops) will not benefit from CUDA either. Corroborated by
the static set of core TUs that actually call the GEMM/GEMV linalg path.

Output: `docs/benchmarks/cuda_diagnostic.md`.
"""
from __future__ import annotations

import argparse
import sys
from collections import defaultdict
from pathlib import Path

REPO = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(REPO / "docs" / "_extras"))
DEFAULT_RESULTS = REPO / "benchmarks" / "cross_binding" / "results"
DEFAULT_OUT = REPO / "docs" / "benchmarks" / "cuda_diagnostic.md"

# Core TUs that call the GEMM/GEMV linalg path (structurally GEMM-bound).
GEMM_TUS = {"aom_selection.cpp", "gpr_pls.cpp", "kernel_pls.cpp", "mb_pls.cpp",
            "model.cpp", "multiblock_extensions.cpp"}

BENEFIT, MARGINAL, NA = "benefits", "marginal", "not_applicable"

# CUDA kernel-launch + host↔device copy is tens of µs to ~1 ms, so a sub-ms op
# can NEVER win on GPU regardless of its (noisy) BLAS speedup. Floor first, then
# look at the speedup. The op must also scale (grow with size) or already be
# clearly heavy to be a real candidate at production sizes.
MIN_MS = 1.0       # below this: timing noise + CUDA overhead dominates → N/A
HEAVY_MS = 5.0     # clearly heavy even at the small dashboard sizes


def classify(speedup: float, blas_omp_ms: float, grows: bool) -> str:
    if blas_omp_ms < MIN_MS:
        return NA  # sub-ms: speedup is noise and CUDA can't amortise its overhead
    if speedup >= 1.4 and (grows or blas_omp_ms >= HEAVY_MS):
        return BENEFIT
    if speedup >= 1.2:
        return MARGINAL
    return NA


def analyse(results_dir: Path) -> list[dict]:
    from build_landing import build_payload
    rows = build_payload(results_dir)["payload"]["rows"]
    # per method: {(n,p): (native_ms, blasomp_ms)} at threads==1
    per: dict[str, dict] = defaultdict(dict)
    for r in rows:
        if int(r["threads"]) != 1:
            continue
        nat = r["cells"].get("pls4all.cpp.native", {}).get("ms")
        bo = r["cells"].get("pls4all.cpp.blas+omp", {}).get("ms")
        if isinstance(nat, (int, float)) and isinstance(bo, (int, float)) and bo > 0:
            per[r["algo"]][(int(r["n"]), int(r["p"]))] = (float(nat), float(bo))
    out = []
    for algo, sizes in per.items():
        if not sizes:
            continue
        big = max(sizes, key=lambda np_: np_[0] * np_[1])  # largest n×p
        nat, bo = sizes[big]
        speedup = nat / bo
        # trend: does the speedup grow with size? (GEMM amortises at scale)
        sp_sorted = [sizes[k][0] / sizes[k][1] for k in sorted(sizes, key=lambda x: x[0] * x[1])]
        grows = len(sp_sorted) >= 2 and sp_sorted[-1] > sp_sorted[0] + 0.1
        out.append({
            "method": algo, "size": f"{big[0]}x{big[1]}",
            "native_ms": round(nat, 4), "blas_omp_ms": round(bo, 4),
            "speedup": round(speedup, 2), "grows_with_size": grows,
            "bucket": classify(speedup, bo, grows),
        })
    return sorted(out, key=lambda d: (-d["speedup"], d["method"]))


def render_md(rows: list[dict]) -> str:
    buckets = {BENEFIT: [], MARGINAL: [], NA: []}
    for r in rows:
        buckets[r["bucket"]].append(r)
    L = ["# CUDA benefit diagnostic",
         "",
         "_Generated by `benchmarks/cross_binding/cuda_diagnostic.py`. The CUDA "
         "(cuBLAS) backend now BUILDS (`cuda-on` preset) and is verified "
         "bit-identical to the CPU reference on GPU (rmse_rel = 0 on "
         "pcr/pls/mb_pls/gpr_pls, RTX 4090/5090); this report prioritises **where "
         "it pays off**._",
         "",
         "## Method", "",
         "Proxy = BLAS+OpenMP vs native-scalar speedup at the largest measured "
         "n×p (BLAS and CUDA accelerate the same GEMM/GEMV path). An absolute "
         f"floor is applied FIRST: any op under {MIN_MS:.0f} ms is "
         "`not_applicable` regardless of its (noisy) speedup — CUDA launch + "
         "host↔device copy overhead alone exceeds it. Above the floor: "
         "`benefits` = speedup ≥ 1.4 and (grows with size or already ≥ "
         f"{HEAVY_MS:.0f} ms); `marginal` = speedup ≥ 1.2; else `not_applicable`. "
         "The backend is built + correctness-verified; a full live GPU timing "
         "sweep (which sizes actually win, given launch + host↔device copy "
         "overhead) is the remaining refinement — treat the buckets below as the "
         "prioritisation for it. Structurally GEMM-bound core TUs: "
         f"`{'`, `'.join(sorted(GEMM_TUS))}`.",
         ""]
    titles = {BENEFIT: "✅ Would benefit from CUDA",
              MARGINAL: "🟡 Marginal / size-dependent",
              NA: "⛔ Not applicable (CUDA would be slower)"}
    for b in (BENEFIT, MARGINAL, NA):
        rs = buckets[b]
        L += [f"## {titles[b]} ({len(rs)})", ""]
        if not rs:
            L += ["_(none)_", ""]
            continue
        L += ["| method | size | native ms | blas+omp ms | speedup | grows w/ size |",
              "|---|---|---|---|---|---|"]
        for r in rs:
            L.append(f"| `{r['method']}` | {r['size']} | {r['native_ms']} | "
                     f"{r['blas_omp_ms']} | {r['speedup']}× | "
                     f"{'yes' if r['grows_with_size'] else 'no'} |")
        L.append("")
    L += ["## Caveats", "",
          "- Proxy only: a real CUDA backend must beat host↔device transfer + "
          "launch latency, which the BLAS proxy doesn't capture. CUDA typically "
          "wins only well above the dashboard sizes (large n and/or p).",
          "- Methods in the `benefits` bucket that also `grow with size` are the "
          "highest-value CUDA targets (kernel/Gram-matrix PLS, GPR-PLS, "
          "multi-block, SVD/PCR, AOM selection).",
          "- Re-run after adding larger benchmark sizes or live `cuda-on` "
          "timings for a sharper verdict.", ""]
    return "\n".join(L)


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--results", type=Path, default=DEFAULT_RESULTS)
    ap.add_argument("--out", type=Path, default=DEFAULT_OUT)
    args = ap.parse_args(argv)
    rows = analyse(args.results)
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(render_md(rows))
    from collections import Counter
    c = Counter(r["bucket"] for r in rows)
    print(f"CUDA diagnostic → {args.out}")
    print(f"  {c.get(BENEFIT, 0)} benefit · {c.get(MARGINAL, 0)} marginal · "
          f"{c.get(NA, 0)} not-applicable  (of {len(rows)} methods with both tiers)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
