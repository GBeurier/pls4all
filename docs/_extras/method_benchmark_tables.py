"""Per-method benchmark table refresh — Sphinx build-time hook.

Each `docs/methods/<name>.md` page committed to the repo contains a
``### Benchmarks`` section generated by ``docs/_extras/build_methods.py``
from ``benchmarks/cross_binding/results/full_matrix.csv``. That section
goes stale the moment a new benchmark run lands.

This module re-renders the section on every ``sphinx-build`` so the
published docs always reflect the latest CSV, *without* requiring users
to remember to re-run ``build_methods.py``. The hook is wired up in
``docs/conf.py:setup()``.

Data-flow priority (mirrors the landing dashboard's strategy):

  1. Live ``full_matrix.csv``       → render fresh, also write snapshot.
  2. Committed ``method-benchmarks.json``     → load snapshot only.
  3. Neither                                  → leave markdown untouched.

The snapshot keeps Read the Docs / fresh-clone builds rendering the
last known-good tables when LFS / benchmark CSVs aren't present.
"""
from __future__ import annotations

import json
import logging
import re
import sys
from pathlib import Path

# build_methods.py lives in the same _extras directory; reuse its
# table renderer + CSV parser rather than maintaining a second copy.
sys.path.insert(0, str(Path(__file__).resolve().parent))

ROOT = Path(__file__).resolve().parents[2]
CSV_PATH = ROOT / "benchmarks" / "cross_binding" / "results" / "full_matrix.csv"
# Snapshot lives in _extras (not _static) so the 430 KB JSON is not
# shipped with the public site — only the renderer needs it.
SNAPSHOT_PATH = ROOT / "docs" / "_extras" / "method-benchmarks.json"

# Each committed methods page ends with the generator's footer:
#   ::::    ← end of parity tab-set
#   <blank>
#   ---
#   <blank>
#   _See also_: ...
# We replace from the ``### Benchmarks`` heading up to (but not
# including) that footer terminator. The ``\s*`` runs absorb the
# blank lines between ``---`` and ``_See also_``. Fallback: end of
# document, for hand-written pages that don't follow the footer
# convention.
_SECTION_RE = re.compile(
    r"^### Benchmarks\b.*?(?=\n---\s*\n\s*_See also_:|\Z)",
    re.MULTILINE | re.DOTALL,
)

_LOG = logging.getLogger(__name__)


def _import_build_methods():
    """Import build_methods lazily.

    Importing eagerly at module load would force the heavy registry
    import (and its optional numpy / sklearn deps) onto every Sphinx
    invocation even when there's no CSV to render. Lazy import keeps
    the snapshot-only path lightweight.
    """
    import build_methods  # type: ignore[import-not-found]
    return build_methods


def build_blocks_from_csv(csv_path: Path = CSV_PATH) -> dict[str, str]:
    """Render the benchmark section markdown for every method with rows.

    Returns ``{method_name: section_markdown}``. Methods with no rows
    in the CSV are absent from the result (so the source-read hook
    leaves their committed page untouched).
    """
    bm = _import_build_methods()
    rows_by_method = bm.parse_csv(csv_path)
    truth_sources = bm.load_truth_source_metadata(strict=False)
    blocks: dict[str, str] = {}
    for method, rows in rows_by_method.items():
        if not rows:
            continue
        block = bm.parity_table(
            method, rows,
            truth_sources=truth_sources.get(method, {}),
        )
        blocks[method] = block.rstrip() + "\n"
    return blocks


def save_snapshot(blocks: dict[str, str],
                   path: Path = SNAPSHOT_PATH) -> bool:
    """Persist rendered blocks so RTD/CI builds can re-use them.

    Returns True on success. A read-only source tree should not fail
    the docs build — log a warning and continue with the in-memory
    blocks instead.
    """
    payload = {
        "schema": 1,
        "source": "build_methods.parity_table",
        "blocks": blocks,
    }
    try:
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(json.dumps(payload, indent=2, ensure_ascii=False),
                         encoding="utf-8")
    except OSError as exc:
        _LOG.warning("method-benchmark snapshot write failed (%s); "
                      "continuing with in-memory blocks.", exc)
        return False
    return True


def load_snapshot(path: Path = SNAPSHOT_PATH) -> dict[str, str]:
    """Load a previously-written snapshot. Empty dict on miss."""
    if not path.exists():
        return {}
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return {}
    blocks = data.get("blocks", {})
    return {k: v for k, v in blocks.items() if isinstance(v, str)}


def load_or_build_blocks() -> tuple[dict[str, str], str]:
    """Return ``(blocks, source_tag)`` for the current build.

    Live CSV rows win over the snapshot, but methods absent from a
    partial CSV inherit the snapshot's last-known-good blocks so a
    single-method benchmark refresh doesn't erase the other 72.

    ``source_tag`` reports which path produced the result; callers
    use it for build logging.
    """
    snapshot = load_snapshot()
    if CSV_PATH.exists():
        try:
            fresh = build_blocks_from_csv()
        except Exception as exc:  # pragma: no cover — surface in logs only
            _LOG.warning("method-benchmark refresh from CSV failed "
                          "(%s: %s); falling back to snapshot.",
                          type(exc).__name__, exc)
            return snapshot, "snapshot (csv-render-error)"
        merged = {**snapshot, **fresh}
        if fresh:
            save_snapshot(merged)
        tag = "csv+snapshot" if snapshot and len(fresh) < len(merged) else "csv"
        return merged, tag
    if snapshot:
        return snapshot, "snapshot"
    return {}, "none"


def replace_benchmark_section(markdown: str, new_block: str) -> str:
    """Swap the existing ``### Benchmarks`` section for ``new_block``.

    If the markdown has no Benchmarks heading (e.g. methods/index.md
    or a hand-written page), it is returned unchanged so the hook is
    safe to apply to every source under ``methods/``.
    """
    if "### Benchmarks" not in markdown:
        return markdown
    return _SECTION_RE.sub(new_block.rstrip() + "\n", markdown, count=1)


def install_sphinx_hook(app, blocks: dict[str, str]) -> None:
    """Register the ``source-read`` listener that does the swap.

    ``source`` is a single-element list Sphinx mutates in place; the
    rest of the build pipeline (MyST parser, sphinx-design tabsets,
    search indexer) then sees the refreshed markdown — no HTML
    post-processing or output surgery needed.
    """
    if not blocks:
        return

    def _on_source_read(_app, docname, source):
        if not docname.startswith("methods/"):
            return
        method = docname.split("/", 1)[1]
        block = blocks.get(method)
        if block is None:
            return
        source[0] = replace_benchmark_section(source[0], block)

    app.connect("source-read", _on_source_read)


# Allow standalone invocation:
#   python docs/_extras/method_benchmark_tables.py
# (re-renders the snapshot from the live CSV without going through Sphinx)
if __name__ == "__main__":  # pragma: no cover
    if not CSV_PATH.exists():
        raise SystemExit(f"missing CSV: {CSV_PATH}")
    blocks = build_blocks_from_csv()
    save_snapshot(blocks)
    print(f"wrote {len(blocks)} method-benchmark blocks to "
          f"{SNAPSHOT_PATH.relative_to(ROOT)}")
