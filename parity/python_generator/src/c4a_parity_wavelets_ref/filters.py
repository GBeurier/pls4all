FILTER_BANKS = {
    "haar":  _HAAR,
    "db4":   _DB4,
    "sym4":  _SYM4,
    "coif1": _COIF1,
}


def family_id(name: str) -> int:
    return {"haar": 0, "db4": 1, "sym4": 2, "coif1": 3}[name]


def boundary_mode_id(name: str) -> int:
    return {"periodization": 0, "symmetric": 1, "zero": 2}[name]


def dwt_output_length(n: int, family: str, mode: str) -> int:
    if n <= 0:
        return 0
    L = len(FILTER_BANKS[family][0])
    if mode == "periodization":
        return (n + 1) // 2
    return (n + L - 1) // 2


def dwt_max_level(n: int, family: str) -> int:
    """Mirror ``pywt.dwt_max_level``."""
    if n <= 0:
        return 0
    L = len(FILTER_BANKS[family][0])
    if L <= 1 or n < L - 1:
        return 0
    return int(math.floor(math.log2(n / (L - 1))))
